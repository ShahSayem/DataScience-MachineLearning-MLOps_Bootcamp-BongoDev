def add(num1, num2):
    """Add two numbers."""
    return num1 + num2

def subtract(num1, num2):
    """Subtract two numbers."""
    return num1 - num2

def multiply(num1, num2):
    """Multiply two numbers."""
    return num1 * num2

def divide(num1, num2):
    """Divide two numbers."""
    if num2 == 0:
        return "Can not divide by zero."
    return num1 / num2


